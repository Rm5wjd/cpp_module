#include "Util.hpp"

void printCharData(char c)
{
	std::cout << "char: " << "'" << c << "'" << std::endl;
}

void printIntData(int i)
{
	std::cout << "int: " << i << std::endl;
}

void printFloatData(float f)
{
	std::cout << "float: " << f << "f" << std::endl;
}

void printDoubleData(double d)
{
	std::cout << "double: " << d << std::endl;
}